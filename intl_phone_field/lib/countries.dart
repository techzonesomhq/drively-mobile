class AppIntlCountry {
  final String name;
  final String nameAr;
  final String flag;
  final String code;
  final String dialCode;
  final String regionCode;
  final int minLength;
  final int maxLength;

  const AppIntlCountry({
    required this.name,
    required this.nameAr,
    required this.flag,
    required this.code,
    required this.dialCode,
    required this.minLength,
    required this.maxLength,
    this.regionCode = "",
  });

  String get fullCountryCode {
    return this.dialCode + this.regionCode;
  }

  String get displayCC {
    if (this.regionCode != "") {
      return "${this.dialCode} ${this.regionCode}";
    }
    return this.dialCode;
  }
}
